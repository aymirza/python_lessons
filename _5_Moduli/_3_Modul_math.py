import math

# возведение числа 2 в степень 3
n1 = math.pow(2,3)
print(n1)

# ту же самую операцию можно выполнить так
n2 = 2**3
print(n2)

# квадратный корень числа
print(math.sqrt(9))

# ближайшее наибольшее целое число
print(math.ceil(4.56))

# ближайшее наимешее целое число
print(math.floor(4.56))

radius = 30
# площад круга с радиусом 30
area = math.pi * math.pow(radius,2)
print(area)

# натуральный логарифм числа 10
number = math.log(10,math.e)
print(number)


